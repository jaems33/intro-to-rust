fn main() {
    let mut x = 5;
    println!("The value of x is {}", x);
    x = 6;
    println!("The value of x is {}", x);

    let x = 5;
    println!("The value of x is {}", x);
    let x = x + 1;
    println!("The value of x is {}", x);
    let x = x * 2;
    println!("The value of x is {}", x);

    let spaces = "   ";
    println!("The value of spaces is {}", spaces);
    let spaces = "Weston Road";
    println!("The value of spaces is {}", spaces);
}
